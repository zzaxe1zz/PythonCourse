def suma(op1, op2):
    print(f'El resultado de la suma es: {op1+op2}')


def resta(op1, op2):
    print(f'El resultado de la resta es: {op1-op2}')


def multiplicacion(op1, op2):
    print(f'El resultado de la multiplicacion es: {op1*op2}')


def dividir(op1, op2):
    print(f'El resultado de la division es: {op1/op2}')


def potencia(base, potencia):
    print(f'El numero: {base} elevado a {potencia} es: {base**potencia}')
